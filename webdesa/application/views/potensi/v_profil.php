        <h3>Sejarah</h3>
        <p class="text-justify">Syahdan pada zaman dahulu kala, dikalngan Kediri tinggalah seorang putri yang sakti mandraguna. Putri tersebut bernama Dewi Kilisuci. Dia merupakan Putri dari Resi Gentayu (Raja Airlangga) dengan Dewi Citra Swara. Dewi Kilisuci, seorang putri kerajaan Kediri yang sakti digdaya mencala putra mencala putri. Dia memiliki kesaktian yang luar biasa sehingga dapat mengubah apa saja menjadi nyata. Dewi kilisuci merupakan Rakryan Hino di kerajaan Kediri. Ini berarti bahwa Dewi kilisuci menduduki jabatan yang kelak menggantikan ayahnya sebagai raja Kediri. Tapi dibalik itu semua, dewi Kilisuci menolak segala hal yang berbau keduniawian dan memutuskan untuk menjadi pertapa.</p>
        <p class="text-justify">Selepas meninggalkan kerajaan Kediri, Dewi Kilisuci memutuskan untuk dating kesebuah alas. Pada mulanya alas tersebut belum dihuni oleh manusia. Dewi Kilisuci membabat alas dengan segala kesaktiaan yang dimiliki. Berkat kesaktian Dewi Kilisuci, terbentuklah sebuah desa yang dinamakan Pagerngumbuk. Dalam perkembangannya, Dewi KIlisuci membaginya menjadi tiga bagian. Atas kiprah Dewi Kilisuci, desa tersebut ditempati manusia sehingga ramai, bahero ada kegiatan manusia dan seterusnya sampai sekarang. Suasana desa tersebut semakin ramai karena banyak orang orang berdatangan dan memulai kehidupan yang baru.</p>
        <p class="text-justify">Pada suatu waktu ada bebegluk yang menyerang di seluruh pagerngumbuk. Bebegluk tersebut menyebabkan kesengsaraan da penderitaan bagi warga Pagerngumbuk. Akibat bebegluk tersebut, banyak warga yang sakit lalu meninggal. Semakin hari bebegluk menyebar di dua dusun yakni dusun pager dan dusun ngumbuk. Tanpa diduga, muncullah Syekh Ibnu, seseorang yang berasal dari dataran Arab yakni Yaman. Syekh Ibnu datang ke dusun ngumbuk sebagai pengelana dan ulama. Syekh Ibnu mengusr bebegluk dengan cara ritual agama Islam dan doa-doa. Atas bantua Syekh Ibnu, bebegluk tersebut hilang dari dusun Ngumbuk. Warga merasa tenang dan bahagia. Mereka bisa memulai kehidupan baru dengan tentram tanpa adanya gangguan dari bebegluk. Melihat kondisi warga ngumbuk yang masih mempercayai roh-roh leluhur dan belum tersentuh agama Islam. Timbulah suatu pemikiran, Syekh Ibnu ingin menyebarkan agama islam untuk pertama kalinya di dusun ngumbuk. Pendekatan agam Islam yang dilakukan Syekh Ibnu dapat diterima oleh warga dusun Ngumbuk sehingga banyak yang memeluk agama Islam.</p>
        <p class="text-justify">Sepeninggal Syekh Ibnu dari dusun Ngumbuk. Wabah bebegluk masih merajalela dan merugikan warga dusun pager. Mendengar kejadian itu, Mbah Noyo datang ke dusun Pager untuk mengusir bebegluk tersebut. Mbah Noyo mengerahkan segala kesaktiaan yang dimiliki untuk menghilangkan bebegluk dari dusun Pager. Warga dusun Pager merasa bahagia karena sudah terbebas dari wabah penyakit. Mereka bisa memulai kehidupan dengan tenang dan tentram.</p>
        <p class="text-justify">Pada akhirnya, kehidupan warga Pagerngumbuk menjadi lebih tentram, makmur, sentosa, dan berkecukupan. Desa pagerngumbuk dijaga oleh para ksatria yang mandraguna. Para ksatria tersebut aalah mbah Noyo, mbah Glugo Sari, mbah Glugo Samin, mbah Jalaluddin, dan mbah Joko lelono. Mbah Noyo adalah ksatria perempuan memiliki kesaktian yang luar biasa. Selanjutnya itu, mbah Glugo Sari merupakan ksatria laki-laki yang kedudukannya sebagai pejuang (pembesar) di desa Pagerngumbuk. Mbah Glugo Samin adalah saudaranya mbah Glugo Sari. Mbah Glugo Samin dianggap sebagai ahli kanugrahan (pusatnya kanugrahan) atau ilmu kekuatan dalam di desa Pagerngumbuk. Selain itu, mbah Glugo Samin juga ahli dalam perang (ahli stategi). Mbah Jalaluddinadalah ksatria yang identik menunggangi kerbau, membawa pedang, dan membawa pecut. Selanjutnya itu, mbah Joko Lelono memiliki kedudukan sebagai demang (kepala daerah) di desa Pagerngumbuk. Mbah Joko Lelono adalah seorang pengelana (ahli laku) yang singgah di desa Pagerngumbuk. Mbah Joko Lelono memiliki kesaktian dan dianggap sebagai ksatria dengan tingkatan ilmu tinggi. Mereka turut adil dalam menjaga keamanan dan ketentraman warga desa Pagerngumbuk dari marabahaya suatu waktu akan datang.</p>
        <p class="text-justify">Sementara itu, kebahagiaan juga dialami oleh warga dusun Bendet. Mbah Mustopa  merupakan junjungan di dusun Bendet. Dia memiliki peran penting dalam menjaga dan melindungi dusun Bendet dari segala macam marabahaya yang datang. Pada akhirnya, desa Pagerngumbuk menjadi satu kesatuan yang utuh. Warga Pagerngumbuk hidup selaras dan harmonis. Seluruh wilayah desa pagerngumbuk dijaga oleh para ksatria yang sakti mandraguna sampai sekarang.</p>
